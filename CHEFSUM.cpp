#include <iostream>
using namespace std;
int main() {
		int i,n,t,l,in,small;
		scanf("%d",&t);
		while(t--)
		{
		scanf("%d",&n);
		int b[n];
		for(i=0;i<n;i++)
		scanf("%d",&b[i]);
	
	small=b[0];
	in=0;
	for(l=0;l<n;l++)
	{
		if(small>b[l])
		{
			small=b[l];in=l;
		}
	}
	printf("%d\n",in+1);
        }
	return 0;
} 
